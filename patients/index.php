<script>
    function openChat() {
        if (document.getElementById("screen").style.display === "none"){
            document.getElementById("screen").style.display = "block";
        }
        else{
            document.getElementById("screen").style.display = "none";
        }
        
    }

    $(document).ready(function(){
        $("#message").on("keyup", function(){
            if($("#message").val()){
                $("#send").css("display","block");
            }
            else{
                $("#send").css("display","none");
            }
        });
    });
    
    // click send button
    $("#send").on("click",function(e){
        $userMessage = $("#message").val();
        $showUserMessage = '<div class="chat userMessage">'+ $userMessage +'</div>';
        $("#messageDisplay").append($showUserMessage);

        // ajax start
        $.ajax({
            url: "bot.php",
            type: "POST",
            // sending data
            data: {mesageValue: $userMessage},
            success: function(data){
                //show respond
                $showBotResponse = '<div class = "messageContainer"><div class="chat botMessage">'+data+'</div></div>';
                $("#messageDisplay").append($showBotResponse);
            }
        });
        $("#message").val("");
        $("#send").css("display", "none");
    });
    
    // OR press enter button
    $(document).keypress(function(event){
        var keycode = (event.keyCode ? event.keyCode : event.which);
        if(keycode == '13'){
            $userMessage = $("#message").val();
            $showUserMessage = '<div class="chat userMessage">'+ $userMessage +'</div>';
            $("#messageDisplay").append($showUserMessage);

            // ajax start
            $.ajax({
                url: "bot.php",
                type: "POST",
                // sending data
                data: {mesageValue: $userMessage},
                success: function(data){
                    //show respond
                    $showBotResponse = '<div class = "messageContainer"><div class="chat botMessage">'+data+'</div></div>';
                    $("#messageDisplay").append($showBotResponse);
                }
            });
            $("#message").val("");
            $("#send").css("display", "none");  
            }
        });
</script>
